def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    prompt_dir = OUT_DIR / "deep_research_prompts"
    prompt_dir.mkdir(exist_ok=True)

    status_path = OUT_DIR / "deep_research_status.csv"
    with status_path.open("w", newline="", encoding="utf-8-sig") as file:
        writer = csv.DictWriter(
            file,
            fieldnames=[
                "序号",
                "县级市",
                "上级行政/兵团口径",
                "治理口径提示",
                "Prompt文件",
                "ChatGPT对话URL",
                "Deep Research状态",
                "报告文件",
                "备注",
            ],
        )
        writer.writeheader()
        for index, city in enumerate(CITIES, start=1):
            prompt_path = prompt_dir / f"{index:02d}_{city.name}_deep_research_prompt.md"
            prompt_path.write_text(build_prompt(city), encoding="utf-8")
            writer.writerow(
                {
                    "序号": index,
                    "县级市": city.name,
                    "上级行政/兵团口径": city.prefecture,
                    "治理口径提示": city.governance_note,
                    "Prompt文件": str(prompt_path),
                    "ChatGPT对话URL": "",
                    "Deep Research状态": "待调用",
                    "报告文件": "",
                    "备注": "",
                }
            )

    index_path = OUT_DIR / "README.md"
    index_path.write_text(build_index(status_path, prompt_dir), encoding="utf-8")
    print(f"Generated {len(CITIES)} city prompt pack at {OUT_DIR}")


