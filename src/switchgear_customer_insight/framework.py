def module_names() -> list[str]:
    names: list[str] = []
    for item in FRAMEWORK:
        if item.module not in names:
            names.append(item.module)
    return names


def fields_by_module() -> dict[str, list[InsightField]]:
    grouped: dict[str, list[InsightField]] = {}
    for item in FRAMEWORK:
        grouped.setdefault(item.module, []).append(item)
    return grouped


def write_field_register(path: str | Path) -> None:
    output = Path(path)
    output.parent.mkdir(parents=True, exist_ok=True)
    with output.open("w", encoding="utf-8-sig", newline="") as handle:
        writer = csv.writer(handle)
        writer.writerow(["模块", "类别", "字段", "说明", "公开资料状态", "内部补充责任人", "备注"])
        for item in FRAMEWORK:
            writer.writerow([item.module, item.category, item.field, item.description, "", "", ""])
